    private void signUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpBtnActionPerformed
        Files f = new Files();
        registro = new File("C:\\Block-Pay\\registros.txt");
        f.searchOrCreateFile(registro, "registros.txt");
        
        int id = (int) Math.floor(Math.random()*(54321-1+1)+1);
        
        while (!f.searchInFile(registro,id)){
            id = (int) Math.floor(Math.random()*(54321-1+1)+1);
        }
        
        Persona p = new Persona(this.userSU.getText(), this.nameSU.getText(), this.lastNameSU.getText(), id, Float.parseFloat(this.moneySU.getText()));

        f.writeFile(registro, p.getUserName(), p.getNames(), p.getLastNames(),
                p.getId(), p.getDinero());
    }//GEN-LAST:event_signUpBtnActionPerformed

