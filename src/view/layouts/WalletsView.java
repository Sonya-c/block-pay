    private void loadWallets() {
        walletsPanel.removeAll();
        
        List<Wallet> wallets = account.getWallets();
        
        for (Wallet wallet : wallets) {
            walletsPanel.add(new WalletCard(wallet));
        }
        
        walletsPanel.validate();
        walletsPanel.repaint();
    }
    
